# შექმენი str ტიპის ცვლადი ნივთის სახელისთვის და float ტიპის ცვლადი ფასისთვის. დაბეჭდე წინადადება, რომელიც აერთიანებს ორივეს.

# ცვლადების შექმნა
item_name = "ლეპტოპი"  # str ტიპის ცვლადი
item_price = 1299.99   # float ტიპის ცვლადი

# შედეგის დაბეჭდვა f-სტრინგის გამოყენებით
print(f"ნივთი: {item_name}, ფასი: {item_price:.2f} ლარი.")
