# შექმენი ორი float ტიპის ცვლადი: სიგრძე და სიგანე. გამოთვალე მართკუთხედის ფართობი და დაბეჭდე.

# სიგრძისა და სიგანის ცვლადების შექმნა
length = 12.5  # სიგრძე მეტრებში
width = 7.8    # სიგანე მეტრებში

# ფართობის გამოთვლა
area = length * width

# შედეგის დაბეჭდვა
print(f"მართკუთხედის ფართობია {area} კვადრატული მეტრი.")
