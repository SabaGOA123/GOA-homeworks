# 2)შექმენი 5 ცვლადი , თითოეულ ცვლადში შეინახე განსხვავებული ტიპის მონაცემი შემდეგ კი გაიგე თითოეულის ტიპი , ფუნქციის გამოყენებით რომელიც ვისწავლეთ დღეს 


name = "საბა"  # სტრინგი (str)
age = 14       # მთელი რიცხვი (int)
height = 1.72  # float ტიპის მაგალითად (ჩავამატე)
is_student = True  # ბულიანი (bool)
hobbies = ["ფეხბურთის თამაში", "ვიდეო თამაშების თამაში"]  # სია (list)

# თითოეული ცვლადის ტიპის გაგება type() ფუნქციით
print("name ტიპია:", type(name))        # <class 'str'>
print("age ტიპია:", type(age))          # <class 'int'>
print("height ტიპია:", type(height))    # <class 'float'>
print("is_student ტიპია:", type(is_student))  # <class 'bool'>
print("hobbies ტიპია:", type(hobbies))  # <class 'list'>
