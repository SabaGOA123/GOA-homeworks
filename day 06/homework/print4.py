# მომხმარებელს შემოატანინე 2 რიცხვი ეს რიცხვები შეინახე 2 განსხვავებულ ცვლადში (num1 , num2) და შეასრულე ამ რიცხვებზე ყველა მათემატიკური ოპერაცია რაც გვაქვს გავლილი

num1 = (input("10"))
num2 = (input("5"))

sum_result = num1 + num2         # შეკრება
difference = num1 - num2         # გამოკლება
product = num1 * num2            # გამრავლება
division = num1 / num2           # გაყოფა
modulus = num1 % num2            # ნაშთი გაყოფისას
power = num1 ** num2             # ხარისხში აყვანა

print("შეკრება:", sum_result)
print("გამოკლება:", difference)
print("გამრავლება:", product)
print("გაყოფა:", division)
print("ნაშთი გაყოფისას:", modulus)
print("ხარისხში აყვანა:", power)
